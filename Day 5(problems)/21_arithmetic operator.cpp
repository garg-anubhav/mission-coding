// binary operator

#include <iostream>
using namespace std;
int main()
{
    int x = 20, y = 10, p, q, r, s, t, u, v;
    p = x + y;
    q = x - y;
    r = x * y;
    s = x / y;
    u = x % y;
    cout << p << "\n"
         << q << "\n"
         << r << "\n"
         << s << "\n"
         << t << "\n"
         << u << "\n"
         << v;
    return 0;
}